int ans;
    vector<vector<int>> dp;
int solve(int n, int m, vector<int> &nums1, vector<int> &nums2) {
        if(n == 0 || m == 0)
            return 0;
        
        if(dp[n][m] != -1)
            return dp[n][m];
        
        if(nums1[n-1] == nums2[m-1]) {
            int x = solve(n-1,m-1,nums1,nums2);
            int y = solve(n-1,m,nums1,nums2);
            int z = solve(n,m-1,nums1,nums2);
            ans = max(ans, max(1+x,max(y,z)));
            return dp[n][m] = 1+x;
        }else{
            int x = solve(n-1,m,nums1,nums2);
            int y = solve(n,m-1,nums1,nums2);
            ans = max(ans, max(x,y));
            return dp[n][m] = 0;
        }
    }
    int findLength(vector<int> nums1, vector<int> nums2) {
        int n = nums1.size();
        int m = nums2.size();
        ans = 0;
        dp = vector<vector<int>>(n+1, vector<int>(m+1, -1));
        solve(n,m,nums1,nums2);
        return ans;
    }

